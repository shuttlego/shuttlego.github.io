def reset_identity_sequences(table_names: list[str] | tuple[str, ...] | None = None) -> None:
    if not is_postgres_backend():
        return
    allowed = set(_IDENTITY_TABLES)
    names = list(table_names) if table_names is not None else sorted(allowed)
    with _conn_ctx() as conn:
        for raw_name in names:
            table_name = str(raw_name or "").strip()
            if table_name not in allowed:
                continue
            conn.execute(
                f"""
                SELECT setval(
                  pg_get_serial_sequence('{table_name}', 'id'),
                  COALESCE((SELECT MAX(id) FROM {table_name}), 1),
                  COALESCE((SELECT MAX(id) FROM {table_name}), 0) > 0
                )
                """
            )
        conn.commit()


def _connect():
    if is_postgres_backend():
        return _PostgresCompatConnection(_postgres_dsn())
    conn = sqlite3.connect(str(get_db_path()), timeout=10, check_same_thread=False)
    conn.row_factory = sqlite3.Row
    conn.execute("PRAGMA foreign_keys=ON")
    conn.execute("PRAGMA busy_timeout=5000")
    return conn


@contextmanager
def _conn_ctx() -> Iterator[Any]:
    conn = _connect()
    try:
        yield conn
    finally:
        conn.close()


