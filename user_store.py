def update_arrival_report_time(
    user_id: int | None,
    report_id: int,
    arrival_time: str,
    *,
    visitor_id: str | None = None,
    max_age_sec: int = ARRIVAL_REPORT_EDIT_WINDOW_SEC,
    allowed_past_sec: int | None = None,
    allowed_future_sec: int | None = None,
) -> dict | None:
    _ensure_writes_allowed()
    time_value = str(arrival_time or "").strip()
    parts = time_value.split(":")
    if len(parts) != 2:
        raise ValueError("도착 시각은 HH:MM 형식이어야 합니다.")
    try:
        hour = int(parts[0])
        minute = int(parts[1])
    except (TypeError, ValueError):
        raise ValueError("도착 시각은 HH:MM 형식이어야 합니다.")
    if hour < 0 or hour > 23 or minute < 0 or minute > 59:
        raise ValueError("도착 시각은 HH:MM 형식이어야 합니다.")

    with _conn_ctx() as conn:
        if user_id is not None:
            row = conn.execute(
                """
                SELECT *
                FROM arrival_reports
                WHERE id = ? AND user_id = ?
                LIMIT 1
                """,
                (int(report_id), int(user_id)),
            ).fetchone()
        else:
            normalized_visitor_id = str(visitor_id or "").strip()[:64] or None
            if not normalized_visitor_id:
                return None
            row = conn.execute(
                """
                SELECT *
                FROM arrival_reports
                WHERE id = ?
                  AND visitor_id = ?
                  AND user_id IS NULL
                LIMIT 1
                """,
                (int(report_id), normalized_visitor_id),
            ).fetchone()
        if row is None:
            return None

        current_item = _row_to_arrival_report(row)
        if not current_item:
            return None
        server_dt = parse_iso(str(current_item.get("server_received_at") or "")) or utc_now()
        if max_age_sec > 0:
            elapsed_sec = max(0.0, (utc_now() - server_dt).total_seconds())
            if elapsed_sec > float(max_age_sec):
                raise ValueError("제보 후 3분 안에만 수정할 수 있습니다.")
        client_dt = parse_iso(str(current_item.get("client_reported_at") or ""))
        if client_dt is None:
            client_dt = utc_now()
        local_dt = client_dt.astimezone(_KST).replace(
            hour=hour,
            minute=minute,
            second=0,
            microsecond=0,
        )
        updated_client_dt = local_dt.astimezone(timezone.utc)
        if allowed_past_sec is not None or allowed_future_sec is not None:
            window_now = utc_now()
            delta_sec = (updated_client_dt - window_now).total_seconds()
            max_past = float(allowed_past_sec if allowed_past_sec is not None else 0)
            max_future = float(allowed_future_sec if allowed_future_sec is not None else 0)
            if delta_sec < (-max_past) or delta_sec > max_future:
                raise ValueError("정류장 도착 시간은 현재 시각 기준 10분 전부터 3분 후까지만 입력할 수 있습니다.")
        service_local_dt = local_dt - timedelta(days=1) if local_dt.hour < 4 else local_dt
        service_date = service_local_dt.date().isoformat()
        reported_clock_minutes = local_dt.hour * 60 + local_dt.minute
        client_server_delta_sec = int(abs((server_dt - updated_client_dt).total_seconds()))

        conn.execute(
            """
            UPDATE arrival_reports
            SET client_reported_at = ?,
                service_date = ?,
                reported_clock_minutes = ?,
                client_server_delta_sec = ?,
                time_valid = 1
            WHERE id = ? AND user_id = ?
            """,
            (
                utc_iso(updated_client_dt),
                service_date,
                int(reported_clock_minutes),
                int(client_server_delta_sec),
                int(report_id),
                int(user_id),
            ),
        ) if user_id is not None else conn.execute(
            """
            UPDATE arrival_reports
            SET client_reported_at = ?,
                service_date = ?,
                reported_clock_minutes = ?,
                client_server_delta_sec = ?,
                time_valid = 1
            WHERE id = ? AND visitor_id = ? AND user_id IS NULL
            """,
            (
                utc_iso(updated_client_dt),
                service_date,
                int(reported_clock_minutes),
                int(client_server_delta_sec),
                int(report_id),
                str(visitor_id or "").strip()[:64],
            ),
        )
        conn.execute(
            "DELETE FROM arrival_report_likes WHERE report_id = ?",
            (int(report_id),),
        )
        conn.commit()
    return get_arrival_report_item(int(report_id))


