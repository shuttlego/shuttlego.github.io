def get_recent_arrival_report_hint(
    user_id: int | None,
    visitor_id: str | None,
    *,
    site_id: str | None = None,
    day_type: str | None = None,
    route_ids: list[int] | None = None,
    route_uuids: list[str] | None = None,
    within_hours: float | int | None = 4,
) -> dict | None:
    normalized_route_ids: list[int] = []
    seen: set[int] = set()
    if route_ids is not None:
        for raw in route_ids:
            try:
                route_id = int(raw)
            except (TypeError, ValueError):
                continue
            if route_id <= 0 or route_id in seen:
                continue
            seen.add(route_id)
            normalized_route_ids.append(route_id)
        if not normalized_route_ids:
            route_ids = []

    normalized_route_uuids: list[str] = []
    seen_uuids: set[str] = set()
    if route_uuids is not None:
        for raw in route_uuids:
            route_uuid = str(raw or "").strip()
            if not route_uuid or route_uuid in seen_uuids:
                continue
            seen_uuids.add(route_uuid)
            normalized_route_uuids.append(route_uuid)

    if (route_ids is not None or route_uuids is not None) and (
        not normalized_route_ids and not normalized_route_uuids
    ):
        return None

    params: list[object] = []
    clauses = ["deleted_at IS NULL"]
    if user_id is not None:
        clauses.append("user_id = ?")
        params.append(int(user_id))
    else:
        clean_visitor_id = str(visitor_id or "").strip()[:64]
        if not clean_visitor_id:
            return None
        clauses.append("visitor_id = ?")
        clauses.append("user_id IS NULL")
        params.append(clean_visitor_id)

    clean_site_id = str(site_id or "").strip()
    if clean_site_id:
        clauses.append("site_id = ?")
        params.append(clean_site_id)

    clean_day_type = str(day_type or "").strip()
    if clean_day_type:
        clauses.append("day_type = ?")
        params.append(clean_day_type)

    if within_hours is not None:
        try:
            hours_value = float(within_hours)
        except (TypeError, ValueError):
            hours_value = 0.0
        if hours_value > 0:
            threshold = utc_iso(utc_now() - timedelta(hours=hours_value))
            clauses.append("server_received_at >= ?")
            params.append(threshold)

    route_filter_clauses: list[str] = []
    if normalized_route_uuids:
        placeholders = ",".join("?" for _ in normalized_route_uuids)
        route_filter_clauses.append(f"route_uuid IN ({placeholders})")
        params.extend(normalized_route_uuids)
    if normalized_route_ids:
        placeholders = ",".join("?" for _ in normalized_route_ids)
        route_filter_clauses.append(f"route_id IN ({placeholders})")
        params.extend(normalized_route_ids)
    if route_filter_clauses:
        clauses.append(f"({' OR '.join(route_filter_clauses)})")

    query = f"""
        SELECT site_id, day_type, route_id, route_uuid, departure_time, stop_id, stop_uuid, server_received_at
        FROM arrival_reports
        WHERE {" AND ".join(clauses)}
        ORDER BY server_received_at DESC, id DESC
        LIMIT 1
    """
    with _conn_ctx() as conn:
        row = conn.execute(query, tuple(params)).fetchone()
    if row is None:
        return None
    return {
        "site_id": str(row["site_id"] or "").strip(),
        "day_type": str(row["day_type"] or "").strip(),
        "route_id": int(row["route_id"]),
        "route_uuid": (str(row["route_uuid"] or "").strip() or None),
        "departure_time": str(row["departure_time"] or "").strip(),
        "stop_id": int(row["stop_id"]),
        "stop_uuid": (str(row["stop_uuid"] or "").strip() or None),
        "server_received_at": str(row["server_received_at"] or "").strip(),
    }


