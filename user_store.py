def list_my_arrival_report_basic_index(
    user_id: int | None,
    *,
    visitor_id: str | None = None,
) -> dict:
    with _conn_ctx() as conn:
        items = _list_all_my_arrival_reports(conn, user_id, visitor_id=visitor_id)
    groups_by_key: dict[str, dict] = {}
    for item in items:
        key = _arrival_report_browser_basic_group_key(item)
        if not key:
            continue
        group = groups_by_key.get(key)
        if group is None:
            group = {
                "key": key,
                "route_id": int(item.get("route_id") or 0),
                "route_uuid": (str(item.get("route_uuid") or "").strip() or None),
                "route_name": str(item.get("route_name") or "").strip(),
                "day_type": str(item.get("day_type") or "").strip(),
                "departure_time": str(item.get("departure_time") or "").strip(),
                "stop_id": int(item.get("stop_id") or 0),
                "stop_uuid": (str(item.get("stop_uuid") or "").strip() or None),
                "stop_name": str(item.get("stop_name") or "").strip(),
                "latest_client_reported_at": str(item.get("client_reported_at") or "").strip(),
                "item_count": 0,
                "credit_total": 0.0,
            }
            groups_by_key[key] = group
        group["item_count"] = int(group.get("item_count") or 0) + 1
        group["credit_total"] = round(
            float(group.get("credit_total") or 0.0)
            + max(0.0, float(item.get("credit_value") or 0.0)),
            6,
        )
        client_reported_at = str(item.get("client_reported_at") or "").strip()
        if client_reported_at > str(group.get("latest_client_reported_at") or ""):
            group["latest_client_reported_at"] = client_reported_at
    groups = list(groups_by_key.values())
    groups.sort(
        key=lambda entry: (
            str(entry.get("latest_client_reported_at") or ""),
            int(entry.get("route_id") or 0),
            int(entry.get("stop_id") or 0),
        ),
        reverse=True,
    )
    return {"groups": groups}


def list_my_arrival_report_route_index(
    user_id: int | None,
    *,
    visitor_id: str | None = None,
) -> dict:
    with _conn_ctx() as conn:
        items = _list_all_my_arrival_reports(conn, user_id, visitor_id=visitor_id)
    routes_by_key: dict[str, dict] = {}
    for item in items:
        route_key = _arrival_report_route_key(item)
        if not route_key:
            continue
        route_group = routes_by_key.get(route_key)
        if route_group is None:
            route_group = {
                "key": route_key,
                "route_id": int(item.get("route_id") or 0),
                "route_uuid": (str(item.get("route_uuid") or "").strip() or None),
                "route_name": str(item.get("route_name") or "").strip(),
                "latest_client_reported_at": str(item.get("client_reported_at") or "").strip(),
                "item_count": 0,
                "stop_count": 0,
                "credit_total": 0.0,
                "stop_map": {},
            }
            routes_by_key[route_key] = route_group
        route_group["item_count"] = int(route_group.get("item_count") or 0) + 1
        route_group["credit_total"] = round(
            float(route_group.get("credit_total") or 0.0)
            + max(0.0, float(item.get("credit_value") or 0.0)),
            6,
        )
        client_reported_at = str(item.get("client_reported_at") or "").strip()
        if client_reported_at > str(route_group.get("latest_client_reported_at") or ""):
            route_group["latest_client_reported_at"] = client_reported_at

        stop_key = _arrival_report_stop_key(item)
        stop_group = route_group["stop_map"].get(stop_key)
        if stop_group is None:
            stop_group = {
                "key": stop_key,
                "stop_id": int(item.get("stop_id") or 0),
                "stop_uuid": (str(item.get("stop_uuid") or "").strip() or None),
                "stop_name": str(item.get("stop_name") or "").strip(),
                "latest_client_reported_at": client_reported_at,
                "item_count": 0,
                "departure_map": {},
            }
            route_group["stop_map"][stop_key] = stop_group
        stop_group["item_count"] = int(stop_group.get("item_count") or 0) + 1
        if client_reported_at > str(stop_group.get("latest_client_reported_at") or ""):
            stop_group["latest_client_reported_at"] = client_reported_at

        departure_key = _arrival_report_browser_route_departure_key(item)
        departure_group = stop_group["departure_map"].get(departure_key)
        if departure_group is None:
            departure_group = {
                "key": departure_key,
                "day_type": str(item.get("day_type") or "").strip(),
                "departure_time": str(item.get("departure_time") or "").strip(),
                "latest_client_reported_at": client_reported_at,
                "item_count": 0,
                "credit_total": 0.0,
            }
            stop_group["departure_map"][departure_key] = departure_group
        departure_group["item_count"] = int(departure_group.get("item_count") or 0) + 1
        departure_group["credit_total"] = round(
            float(departure_group.get("credit_total") or 0.0)
            + max(0.0, float(item.get("credit_value") or 0.0)),
            6,
        )
        if client_reported_at > str(departure_group.get("latest_client_reported_at") or ""):
            departure_group["latest_client_reported_at"] = client_reported_at

    routes: list[dict] = []
    for route_group in routes_by_key.values():
        stop_groups: list[dict] = []
        for stop_group in route_group["stop_map"].values():
            departure_groups = list(stop_group["departure_map"].values())
            departure_groups.sort(
                key=lambda entry: (
                    str(entry.get("latest_client_reported_at") or ""),
                    str(entry.get("departure_time") or ""),
                ),
                reverse=True,
            )
            stop_group["departure_groups"] = departure_groups
            stop_group.pop("departure_map", None)
            stop_groups.append(stop_group)
        stop_groups.sort(
            key=lambda entry: (
                str(entry.get("latest_client_reported_at") or ""),
                int(entry.get("stop_id") or 0),
            ),
            reverse=True,
        )
        route_group["stop_groups"] = stop_groups
        route_group["stop_count"] = len(stop_groups)
        route_group.pop("stop_map", None)
        routes.append(route_group)
    routes.sort(
        key=lambda entry: (
            str(entry.get("latest_client_reported_at") or ""),
            int(entry.get("route_id") or 0),
        ),
        reverse=True,
    )
    return {"routes": routes}


