def _arrival_report_exclude_message(reason: str | None) -> str:
    code = str(reason or "").strip()
    if code == "user_deleted":
        return "사용자 삭제"
    if code == "client_clock_skew":
        return "기기 시각 오차 5분 초과"
    if code == "superseded":
        return "최신 제보로 대체됨"
    if code:
        return code
    return ""


def _arrival_report_like_bonus(qualified_like_count: int) -> float:
    like_count = max(0, int(qualified_like_count or 0))
    if like_count <= 0:
        return 0.0
    return min(
        ARRIVAL_REPORT_LIKE_BONUS_CAP,
        ARRIVAL_REPORT_LIKE_BONUS_FACTOR * math.log2(1.0 + float(like_count)),
    )


def _build_credit_base_value_map(rows: list[dict]) -> dict[int, float]:
    history_by_key: dict[tuple[int, str, str, str, str, str], deque[datetime]] = {}
    value_map: dict[int, float] = {}
    rolling_window = timedelta(days=ARRIVAL_REPORT_CREDIT_WINDOW_DAYS)
    eligible_rows = [
        row
        for row in rows
        if row.get("user_id") is not None and not row.get("deleted_at")
    ]
    for row in sorted(eligible_rows, key=lambda item: (str(item.get("server_received_at") or ""), int(item.get("id") or 0))):
        report_id = int(row.get("id") or 0)
        if report_id <= 0:
            continue
        row_dt = parse_iso(str(row.get("server_received_at") or "")) or utc_now()
        credit_key = _arrival_report_credit_key(row)
        history = history_by_key.setdefault(credit_key, deque())
        cutoff = row_dt - rolling_window
        while history and history[0] < cutoff:
            history.popleft()
        repeat_count = len(history)
        value_map[report_id] = max(
            ARRIVAL_REPORT_CREDIT_MIN_VALUE,
            0.5 ** float(repeat_count),
        )
        history.append(row_dt)
    return value_map


def _build_latest_eta_id_map(rows: list[dict]) -> dict[tuple[str, str, str, int, str, int, str], int]:
    latest_eta_ids: dict[tuple[str, str, str, str, str, str, str], int] = {}
    eligible_rows = [
        row
        for row in rows
        if not row.get("deleted_at")
    ]
    for row in sorted(eligible_rows, key=lambda item: (str(item.get("server_received_at") or ""), int(item.get("id") or 0))):
        group_key = _arrival_report_group_key(row)
        latest_eta_ids[group_key] = int(row.get("id") or 0)
    return latest_eta_ids


def _annotate_arrival_reports(rows: list[dict]) -> list[dict]:
    latest_eta_ids = _build_latest_eta_id_map(rows)

    annotated: list[dict] = []
    for row in rows:
        item = dict(row)
        group_key = _arrival_report_group_key(row)
        deleted = bool(row.get("deleted_at"))
        eta_included = (not deleted) and latest_eta_ids.get(group_key) == int(row["id"])
        credit_value = 0.0 if deleted else float(row.get("credit_value") or 0.0)
        eta_exclude_reason = ""
        eta_excluded = False
        report_state = "active"
        report_state_label = ""
        report_note = ""
        if deleted:
            eta_exclude_reason = _arrival_report_exclude_message(row.get("deleted_reason") or "user_deleted")
            eta_excluded = True
            report_state = "deleted"
            report_state_label = "삭제됨"
            report_note = eta_exclude_reason
        elif not eta_included:
            report_state = "previous"
            report_state_label = "이전 제보"
            report_note = "같은 제보자의 최신 제보만 ETA에 반영됩니다."
        item["eta_included"] = bool(eta_included)
        item["credit_value"] = round(max(0.0, credit_value), 6)
        item["credit_awarded"] = bool(item["credit_value"] > 0.0)
        item["eta_excluded"] = bool(eta_excluded)
        item["eta_exclude_reason"] = eta_exclude_reason
        item["report_state"] = report_state
        item["report_state_label"] = report_state_label
        item["report_note"] = report_note
        annotated.append(item)
    return annotated


