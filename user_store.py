def _load_logged_in_like_counts(
    conn: sqlite3.Connection,
    report_ids: list[int] | tuple[int, ...] | None = None,
) -> dict[int, int]:
    normalized_ids: list[int] = []
    seen: set[int] = set()
    for raw in report_ids or []:
        try:
            report_id = int(raw)
        except (TypeError, ValueError):
            continue
        if report_id <= 0 or report_id in seen:
            continue
        seen.add(report_id)
        normalized_ids.append(report_id)
    params: tuple[object, ...] = ()
    query = """
        SELECT report_id, COUNT(*) AS like_count
        FROM arrival_report_likes
        WHERE user_id IS NOT NULL
    """
    if normalized_ids:
        placeholders = ",".join("?" for _ in normalized_ids)
        query += f" AND report_id IN ({placeholders})"
        params = tuple(normalized_ids)
    query += "\n        GROUP BY report_id\n        "
    rows = conn.execute(query, params).fetchall()
    counts: dict[int, int] = {}
    for row in rows:
        try:
            counts[int(row["report_id"])] = int(row["like_count"] or 0)
        except (TypeError, ValueError):
            continue
    return counts


def _load_eta_like_weight_counts(
    conn: sqlite3.Connection,
    report_ids: list[int] | tuple[int, ...] | None = None,
) -> dict[int, dict[str, int]]:
    normalized_ids: list[int] = []
    seen: set[int] = set()
    for raw in report_ids or []:
        try:
            report_id = int(raw)
        except (TypeError, ValueError):
            continue
        if report_id <= 0 or report_id in seen:
            continue
        seen.add(report_id)
        normalized_ids.append(report_id)
    if not normalized_ids:
        return {}

    placeholders = ",".join("?" for _ in normalized_ids)
    rows = conn.execute(
        f"""
        SELECT
          report_id,
          SUM(CASE WHEN user_id IS NOT NULL THEN 1 ELSE 0 END) AS logged_in_like_count,
          SUM(CASE WHEN user_id IS NULL THEN 1 ELSE 0 END) AS guest_like_count
        FROM arrival_report_likes
        WHERE report_id IN ({placeholders})
        GROUP BY report_id
        """,
        tuple(normalized_ids),
    ).fetchall()
    counts: dict[int, dict[str, int]] = {}
    for row in rows:
        try:
            report_id = int(row["report_id"])
        except (TypeError, ValueError):
            continue
        counts[report_id] = {
            "logged_in_like_count": int(row["logged_in_like_count"] or 0),
            "guest_like_count": int(row["guest_like_count"] or 0),
        }
    return counts


def _build_credit_value_map(
    rows: list[dict],
    *,
    qualified_like_counts: dict[int, int] | None = None,
) -> dict[int, float]:
    base_map = _build_credit_base_value_map(rows)
    qualified_like_counts = qualified_like_counts or {}
    latest_id_set = set(_build_latest_eta_id_map(rows).values())
    value_map: dict[int, float] = {}
    for row in rows:
        report_id = int(row.get("id") or 0)
        if report_id <= 0:
            continue
        base_value = float(base_map.get(report_id, 0.0))
        bonus_value = 0.0
        if report_id in latest_id_set:
            bonus_value = _arrival_report_like_bonus(int(qualified_like_counts.get(report_id, 0) or 0))
        value_map[report_id] = round(base_value + bonus_value, 6)
    return value_map


def get_user_credit_totals(user_ids: list[int] | None = None) -> dict[int, float]:
    requested: set[int] | None = None
    if user_ids is not None:
        requested = set()
        for raw in user_ids:
            try:
                uid = int(raw)
            except (TypeError, ValueError):
                continue
            if uid > 0:
                requested.add(uid)
        if not requested:
            return {}

    with _conn_ctx() as conn:
        credit_rows = _load_credit_rows(conn, requested)
        if not credit_rows:
            return {}
        like_counts = _load_logged_in_like_counts(
            conn,
            [int(row["id"]) for row in credit_rows],
        )
        credit_value_map = _build_credit_value_map(credit_rows, qualified_like_counts=like_counts)

    counts: dict[int, float] = {}
    for row in credit_rows:
        uid = int(row["user_id"])
        if requested is not None and uid not in requested:
            continue
        counts[uid] = round(counts.get(uid, 0.0) + float(credit_value_map.get(int(row["id"]), 0.0)), 6)
    return counts


