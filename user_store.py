def _attach_credit_values_to_items(
    conn: sqlite3.Connection,
    items: list[dict],
    *,
    user_id: int | None = None,
) -> list[dict]:
    if user_id is None or not items:
        for item in items:
            item["credit_value"] = 0.0
        return items
    credit_rows = _load_credit_rows(conn, {int(user_id)})
    if not credit_rows:
        for item in items:
            item["credit_value"] = 0.0
        return items
    like_counts = _load_logged_in_like_counts(
        conn,
        [int(row["id"]) for row in credit_rows],
    )
    credit_value_map = _build_credit_value_map(credit_rows, qualified_like_counts=like_counts)
    for item in items:
        report_id = int(item.get("id") or 0)
        item["credit_value"] = round(float(credit_value_map.get(report_id, 0.0)), 6)
    return items


